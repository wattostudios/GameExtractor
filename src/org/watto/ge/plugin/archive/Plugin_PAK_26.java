package org.watto.ge.plugin.archive;

import java.io.File;

import org.watto.datatype.Resource;
import org.watto.ge.helper.FieldValidator;
import org.watto.ge.plugin.ArchivePlugin;
import org.watto.io.FileManipulator;
import org.watto.task.TaskProgressManager;

/**
**********************************************************************************************

**********************************************************************************************
**/
public class Plugin_PAK_26 extends ArchivePlugin {

  /**
  **********************************************************************************************
  
  **********************************************************************************************
  **/
  public Plugin_PAK_26() {

    super("PAK_26", "PAK_26");

    //         read write replace rename
    setProperties(true, false, false, false);

    setGames("Broken Sword 3: The Sleeping Dragon");
    setExtensions("pak");
    setPlatforms("PC", "XBox");

    setCanScanForFileTypes(true);

    setTextPreviewExtensions("script", "kern", "font"); // LOWER CASE

  }

  /**
  **********************************************************************************************
  
  **********************************************************************************************
  **/
  @Override
  public int getMatchRating(FileManipulator fm) {
    try {

      int rating = 0;

      if (FieldValidator.checkExtension(fm, extensions)) {
        rating += 25;
      }

      // Number Of Files
      if (FieldValidator.checkNumFiles(fm.readInt())) {
        rating += 5;
      }

      // Unknown (22)
      if (fm.readInt() == 22) {
        rating += 5;
      }

      fm.skip(4);

      long arcSize = fm.getLength();

      // First File Offset
      if (FieldValidator.checkOffset(fm.readInt(), arcSize)) {
        rating += 5;
      }

      // First File Length
      if (FieldValidator.checkLength(fm.readInt(), arcSize)) {
        rating += 5;
      }

      return rating;

    }
    catch (Throwable t) {
      return 0;
    }
  }

  /**
   **********************************************************************************************
   * Reads an [archive] File into the Resources
   **********************************************************************************************
   **/
  @Override
  public Resource[] read(File path) {
    try {

      // NOTE - Compressed files MUST know their DECOMPRESSED LENGTH
      //      - Uncompressed files MUST know their LENGTH

      addFileTypes();

      // RESETTING THE GLOBAL VARIABLES

      FileManipulator fm = new FileManipulator(path, false);

      long arcSize = (int) fm.getLength();

      // 4 - Number Of Files
      int numFiles = fm.readInt();
      FieldValidator.checkNumFiles(numFiles);

      // 4 - Unknown (22)
      fm.skip(4);

      Resource[] resources = new Resource[numFiles];

      TaskProgressManager.setMaximum(numFiles);

      // Loop through directory
      for (int i = 0; i < numFiles; i++) {
        // 4 - Hash?
        fm.skip(4);

        // 4 - File Offset
        long offset = fm.readInt();
        FieldValidator.checkOffset(offset, arcSize);

        // 4 - File Length
        long length = fm.readInt();
        FieldValidator.checkLength(length, arcSize);

        // 4 - null
        fm.skip(4);

        String filename = Resource.generateFilename(i);

        //path,id,name,offset,length,decompLength,exporter
        resources[i] = new Resource(path, filename, offset, length);

        TaskProgressManager.setValue(i);
      }

      fm.close();

      return resources;

    }
    catch (Throwable t) {
      logError(t);
      return null;
    }
  }

  /**
  **********************************************************************************************
  If an archive doesn't have filenames stored in it, the scanner can come here to try to work out
  what kind of file a Resource is. This method allows the plugin to provide additional plugin-specific
  extensions, which will be tried before any standard extensions.
  @return null if no extension can be determined, or the extension if one can be found
  **********************************************************************************************
  **/
  @Override
  public String guessFileExtension(Resource resource, byte[] headerBytes, int headerInt1, int headerInt2, int headerInt3, short headerShort1, short headerShort2, short headerShort3, short headerShort4, short headerShort5, short headerShort6) {

    if (headerInt1 == 1314014539) {
      return "kern";
    }
    else if (headerInt1 == 1330004291) {
      return "font";
    }
    else if (headerInt1 == 1717986683) {
      return "script";
    }
    else if (headerShort1 == -257) {
      return "txt";
    }

    // Not sure, there's just lots of these 3 types
    if (headerInt1 == 0) {
      return "type0";
    }
    else if (headerInt1 == 1) {
      return "type1";
    }
    else if (headerInt1 == 27) {
      return "type27";
    }

    int length = (int) resource.getDecompressedLength();

    if (headerInt1 == 16 && headerInt2 == (length - 12)) {
      return "mesh16";
    }
    else if (headerInt1 == 11 && headerInt2 == (length - 12)) {
      return "mesh11";
    }

    else if (headerInt2 == 1 && length > 50 && length < 1200) {
      return "audio_def1";
    }
    else if (headerInt2 == 2 && length > 50 && length < 1200) {
      return "audio_def2";
    }
    else if (headerInt2 == 3 && length > 50 && length < 1200) {
      return "audio_def3";
    }
    else if (headerInt2 == 4 && length > 50 && length < 1200) {
      return "audio_def4";
    }

    return null;
  }

}
